package com.entity;

public class Vip {

	private int vip;
	private int start;
	private int stop;
	private int zheKou;
	
	public int getVip() {
		return vip;
	}
	public void setVip(int vip) {
		this.vip = vip;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getStop() {
		return stop;
	}
	public void setStop(int stop) {
		this.stop = stop;
	}
	public int getZheKou() {
		return zheKou;
	}
	public void setZheKou(int zheKou) {
		this.zheKou = zheKou;
	}
	
	
}
